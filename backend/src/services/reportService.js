  async generatePDFReport(reportData) {
    const doc = new PDFDocument({ margin: 50, size: 'A4' });
    const buffers = [];

    return new Promise((resolve, reject) => {
      doc.on('data', chunk => buffers.push(chunk));
      doc.on('end', () => resolve(Buffer.concat(buffers)));
      doc.on('error', reject);

      const {
        title, generatedBy, trainNumber, coach, contractor,
        periodStart, periodEnd, sections = [], images = []
      } = reportData;

      doc.fontSize(18).font('Helvetica-Bold').text('INDIAN RAILWAYS', { align: 'center' });
      doc.fontSize(14).text('OBHS - Coach & Washroom Cleaning System', { align: 'center' });
      doc.moveDown(0.5);
      doc.fontSize(12).font('Helvetica-Bold').text(title || 'EVIDENCE REPORT', { align: 'center' });
      doc.moveDown();

      doc.fontSize(9).font('Helvetica');
      const metaLines = [
        `Generated By: ${generatedBy || 'System'}`,
        `Period: ${periodStart || 'N/A'} to ${periodEnd || 'N/A'}`
      ];
      if (trainNumber) metaLines.push(`Train: ${trainNumber}`);
      if (coach) metaLines.push(`Coach: ${coach}`);
      if (contractor) metaLines.push(`Contractor: ${contractor}`);

      metaLines.forEach(line => doc.text(line, { align: 'left' }));
      doc.moveDown();

      doc.moveTo(50, doc.y).lineTo(545, doc.y).stroke('#cccccc');
      doc.moveDown();

      for (const section of sections) {
        if (doc.y > 700) doc.addPage();

        doc.fontSize(11).font('Helvetica-Bold').text(section.heading, { underline: true });
        doc.moveDown(0.3);

        if (section.items && section.items.length > 0) {
          const colWidths = section.colWidths || [80, 120, 120, 80, 80];
          const headers = section.headers || ['Field', 'Value'];
          const startX = 50;
          let y = doc.y;

          doc.fontSize(8).font('Helvetica-Bold');
          let x = startX;
          headers.forEach((h, i) => {
            doc.text(h, x, y, { width: colWidths[i], align: 'left' });
            x += colWidths[i];
          });
          doc.moveDown(0.5);
          y = doc.y;
          doc.moveTo(50, y - 2).lineTo(545, y - 2).stroke('#dddddd');

          doc.fontSize(8).font('Helvetica');
          for (const item of section.items) {
            if (doc.y > 720) doc.addPage();
            y = doc.y;
            x = startX;
            const values = section.fields ? section.fields.map(f => item[f] || '') : [item.label || '', item.value || ''];
            values.forEach((v, i) => {
              doc.text(String(v), x, y, { width: colWidths[i], align: 'left' });
              x += colWidths[i];
            });
            doc.moveDown(0.4);
          }
        } else {
          doc.fontSize(9).font('Helvetica').text('No data available', { indent: 20 });
        }
        doc.moveDown();
      }

      if (images.length > 0) {
        if (doc.y > 650) doc.addPage();
        doc.fontSize(11).font('Helvetica-Bold').text('EVIDENCE IMAGES', { underline: true });
        doc.moveDown();

        for (const img of images) {
          if (doc.y > 700) doc.addPage();
          try {
            if (img.path && fs.existsSync(img.path)) {
              const imgWidth = Math.min(250, img.width || 200);
              doc.image(img.path, 50, doc.y, { width: imgWidth });
              doc.moveDown(4);
              doc.fontSize(8).font('Helvetica').text(`${img.caption || ''}`, { indent: 50 });
              doc.moveDown(0.5);
            } else if (img.url) {
              doc.fontSize(8).font('Helvetica').text(`[Image: ${img.caption || img.url}]`, { indent: 20 });
              doc.moveDown(0.3);
            }
          } catch {
            doc.fontSize(8).font('Helvetica').text(`[Image unavailable: ${img.caption || 'unknown'}]`, { indent: 20 });
            doc.moveDown(0.3);
          }
        }
      }

      const footerY = doc.page.height - 50;
      doc.fontSize(7).font('Helvetica').text(
        `Generated on ${new Date().toLocaleString()} | Railway OBHS Evidence Management System`,
        50, footerY, { align: 'center', width: 495 }
      );

      doc.end();
    });
  }

  async getPremisesData(user, query) {
    const { zone, division, depot, startDate, endDate, areaType, contractorId, contractId, supervisorId } = query;
    if (contractId) {
      const contractDoc = await db.collection('contracts').doc(contractId).get();
      if (!contractDoc.exists) throw new NotFoundError('Contract Not Found');
      const cData = contractDoc.data();
      const isStatusExpired = ['Expired', 'expired', 'Inactive', 'inactive'].includes(cData.status);
      let isDateExpired = false;
      if (cData.endDate) { const today = new Date(); const end = new Date(cData.endDate); end.setHours(23, 59, 59, 999); if (today > end) isDateExpired = true; }
      if (isStatusExpired || isDateExpired) throw new ValidationError('Contract Expired', `Expired on ${cData.endDate || 'Unknown'}`);
    }
    let queryRef = db.collection('premisesForms').where('status', 'in', ['LOCKED', 'AUTO-APPROVED']);
    if (zone) queryRef = queryRef.where('submittedByZone', '==', zone);
    if (division) queryRef = queryRef.where('submittedByDivision', '==', division);
    if (depot) queryRef = queryRef.where('submittedByDepot', '==', depot);
    if (contractorId) queryRef = queryRef.where('submittedByEntityId', '==', contractorId);
    if (contractId) queryRef = queryRef.where('contractId', '==', contractId);
    if (supervisorId) queryRef = queryRef.where('submittedById', '==', supervisorId);
    const snapshot = await queryRef.limit(200).get();
    let reportData = [];
    const start = startDate ? new Date(startDate) : null;
    const end = endDate ? new Date(endDate) : null;
    if (end) end.setHours(23, 59, 59, 999);
    const selectedAreas = areaType ? areaType.split(',').map(item => item.trim()) : [];
    const areaMap = { 'GICC': 23530, 'OWS': 8630, 'NWS': 10130 };
    snapshot.forEach(doc => {
      const data = doc.data();
      const formDate = data.formDateTime ? new Date(data.formDateTime) : new Date();
      let includeRecord = true;
      if (start && formDate < start) includeRecord = false;
      if (end && formDate > end) includeRecord = false;
      if (includeRecord && selectedAreas.length > 0 && !selectedAreas.includes(data.location)) includeRecord = false;
      if (includeRecord) {
        const summary = data.ratingDetails?.summary || {};
        const totalAreaSqMtr = data.area || areaMap[data.location] || 0;
        const overallPct = parseFloat(summary.overallAveragePct) || 0;
        let pen8190 = 'NA', pen7180 = 'NA', pen70 = 'NA';
        if (overallPct >= 81 && overallPct <= 90) pen8190 = 'Yes';
        else if (overallPct >= 71 && overallPct <= 80) pen7180 = 'Yes';
        else if (overallPct <= 70) pen70 = 'Yes';
        reportData.push({
          date: formDate.toLocaleDateString('en-IN'),
          premiseName: data.location || 'Unknown',
          location: data.location || 'Unknown',
          areaCategory: totalAreaSqMtr,
          totalArea: totalAreaSqMtr,
          areaAttended: totalAreaSqMtr,
          areaNotAttended: 0,
          ratingInPct: overallPct.toFixed(2) + '%',
          housekeepingScore: summary.housekeepingAveragePct || '0%',
          pitLineScore: summary.pitLineAveragePct || '0%',
          garbageScore: summary.garbageDisposalAveragePct || '0%',
          overallScore: summary.overallAveragePct || '0%',
          above90: overallPct > 90 ? 'Yes' : 'NA',
          penalty81to90: pen8190,
          penalty71to80: pen7180,
          penaltyBelow70: pen70
        });
      }
    });
    return { count: reportData.length, data: reportData };
  }

