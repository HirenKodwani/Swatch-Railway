  async emailReport(user, body) {
    const {
      reportType, subject, bodyHtml, sections, images,
      periodStart, periodEnd, trainNumber, coach, contractor,
      to, cc, bcc
    } = body;

    let sgMail;
    try {
      sgMail = (await import('@sendgrid/mail')).default;
      if (config.sendgrid.apiKey) {
        sgMail.setApiKey(config.sendgrid.apiKey);
      }
    } catch { /* sendgrid not available */ }

    const pdfBuffer = await this.generatePDFReport({
      title: subject || `${reportType} Report`,
      generatedBy: user.fullName,
      trainNumber, coach, contractor,
      periodStart, periodEnd,
      sections: sections || [],
      images: images || []
    });

    const recipients = to && to.length > 0
      ? to
      : [user.email || 'user@unknown.com'];

    const msg = {
      to: recipients,
      cc: cc || [],
      bcc: bcc || [],
      from: config.reporting.fromEmail || 'noreply@swachhrailways.com',
      subject: subject || `${(reportType || 'Report').toUpperCase()} Report - ${new Date().toISOString().split('T')[0]}`,
      html: bodyHtml || `<p>Please find attached the ${reportType} report.</p>`,
      attachments: [{
        content: pdfBuffer.toString('base64'),
        filename: `${reportType || 'report'}_${new Date().toISOString().split('T')[0]}.pdf`,
        type: 'application/pdf',
        disposition: 'attachment'
      }]
    };

    let sendResult = { messageId: 'simulated' };
    if (sgMail && config.sendgrid.apiKey) {
      try { sendResult = await sgMail.send(msg); } catch { /* fallback */ }
    }

    const emailRef = db.collection('email_history').doc();
    await emailRef.set({
      emailId: emailRef.id,
      reportType,
      subject: msg.subject,
      to: msg.to,
      cc: msg.cc,
      sentBy: user.uid,
      sentByName: user.fullName,
      sentByRole: user.role,
      pdfSize: pdfBuffer.length,
      sendgridMessageId: sendResult.messageId || sendResult[0]?.statusCode || 'simulated',
      sentAt: admin.firestore.FieldValue.serverTimestamp()
    });

    await this.logAudit('REPORT_EMAILED', {
      userId: user.uid, userName: user.fullName,
      userRole: user.role, trainNumber,
      details: `Emailed ${reportType} report to ${recipients.join(', ')}`
    });

    return {
      success: true,
      message: 'Report emailed successfully',
      emailId: emailRef.id,
      recipients,
      pdfSize: pdfBuffer.length
    };
  }

