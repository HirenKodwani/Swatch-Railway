  async getCtsStats(user, query) {
    const { uid, role, userType, entityId, division } = user;
    const { startDate, endDate, contractId } = query;
    let queryRef = db.collection('ctsForms').where('status', 'in', ['LOCKED', 'AUTO-APPROVED']);
    const userRole = (role || '').toLowerCase();
    const isMaster = userRole.includes('master');
    const isAdmin = userRole.includes('admin');
    if (userType === 'contractor') {
      if (!entityId) throw new ForbiddenError('Entity ID missing.');
      queryRef = queryRef.where('submittedByEntityId', '==', entityId);
      if (!isMaster && isAdmin) queryRef = queryRef.where('submittedByDivision', '==', division);
      else if (!isMaster && !isAdmin) queryRef = queryRef.where('submittedById', '==', uid);
    } else { if (isAdmin) queryRef = queryRef.where('submittedByDivision', '==', division); }
    if (contractId) queryRef = queryRef.where('contractId', '==', contractId);
    const snapshot = await queryRef.limit(200).get();
    let stats = {
      totalTrains: 0, totalCoachesCleaned: 0, totalUnattendedCoaches: 0,
      totalSampledCoaches: 0, sumSamplingPercentage: 0, windowTimeCount: 0, sumWindowTime: 0,
      grades: { A: 0, B: 0, C: 0, D: 0 },
      ops: { toiletOccupied: 0, toiletUnattended: 0, totalScoreSum: 0, scoredFormsCount: 0 },
      resources: { manpowerTotal: 0, chemicalQuantity: 0, machinesUsed: 0 }
    };
    const start = startDate ? new Date(startDate) : null;
    const end = endDate ? new Date(endDate) : null;
    if (end) end.setHours(23, 59, 59, 999);
    snapshot.forEach(doc => {
      const data = doc.data();
      let include = true;
      if (start || end) { if (!data.formDateTime) include = false; else { const fDate = new Date(data.formDateTime); if (start && fDate < start) include = false; if (end && fDate > end) include = false; } }
      if (include) {
        stats.totalTrains++;
        const attended = Number(data.coachesAttended) || 0;
        const inRake = Number(data.coachesInRake) || 0;
        stats.totalCoachesCleaned += attended;
        stats.totalUnattendedCoaches += (inRake - attended);
        if (data.allowedWindow) { stats.sumWindowTime += Number(data.allowedWindow); stats.windowTimeCount++; }
        stats.resources.manpowerTotal += (data.attendanceStaff ? data.attendanceStaff.length : 0);
        const rating = data.ratingDetails || {};
        const chemicalsArray = rating.chemicals || [];
        chemicalsArray.forEach(c => { stats.resources.chemicalQuantity += (parseFloat(c.quantity) || 0); });
        const summary = rating.summary || {};
        if (summary.overallGrade) {
          const g = summary.overallGrade;
          if (g.includes('(A)')) stats.grades.A++;
          else if (g.includes('(B)')) stats.grades.B++;
          else if (g.includes('(C)')) stats.grades.C++;
          else stats.grades.D++;
          stats.ops.totalScoreSum += (Number(summary.averageScore) || 0);
          stats.ops.scoredFormsCount++;
        }
        const evalTable = rating.coachEvaluationTable || [];
        stats.totalSampledCoaches += evalTable.length;
        if (rating.inspectionHeader) { stats.sumSamplingPercentage += (Number(rating.inspectionHeader.samplingPercentage) || 0); }
        stats.ops.toiletOccupied += (Number(data.occupiedToilets) || 0);
        stats.ops.toiletUnattended += (Number(data.occupiedToilets) || 0);
        if (Array.isArray(rating.machinesUsed)) { stats.resources.machinesUsed += rating.machinesUsed.length; }
      }
    });
    const totalGrades = stats.grades.A + stats.grades.B + stats.grades.C + stats.grades.D || 1;
    const avgScore = stats.ops.scoredFormsCount > 0 ? (stats.ops.totalScoreSum / stats.ops.scoredFormsCount).toFixed(2) : 0;
    const avgWindow = stats.windowTimeCount > 0 ? (stats.sumWindowTime / stats.windowTimeCount).toFixed(0) : 0;
    const avgSamplingPct = stats.totalTrains > 0 ? (stats.sumSamplingPercentage / stats.totalTrains).toFixed(1) : 0;
    return {
      statistics: {
        totalTrains: stats.totalTrains, totalCoachesCleaned: stats.totalCoachesCleaned,
        totalUnattendedCoaches: stats.totalUnattendedCoaches, sampledCoaches: stats.totalSampledCoaches,
        samplingPercentage: `${avgSamplingPct}%`, averageWindowTime: `${avgWindow} Min`
      },
      gradeDistribution: {
        A: { count: stats.grades.A, pct: ((stats.grades.A / totalGrades) * 100).toFixed(1) },
        B: { count: stats.grades.B, pct: ((stats.grades.B / totalGrades) * 100).toFixed(1) },
        C: { count: stats.grades.C, pct: ((stats.grades.C / totalGrades) * 100).toFixed(1) },
        D: { count: stats.grades.D, pct: ((stats.grades.D / totalGrades) * 100).toFixed(1) }
      },
      cleaningOperations: { toiletOccupied: stats.ops.toiletOccupied, averageScore: avgScore, toiletUnattended: stats.ops.toiletUnattended },
      resourcesUsed: { manpowerTotal: stats.resources.manpowerTotal, chemicalQuantity: stats.resources.chemicalQuantity.toFixed(1), machineryUsed: stats.resources.machinesUsed }
    };
  }

